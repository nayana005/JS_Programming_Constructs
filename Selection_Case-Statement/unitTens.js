var number = 10;
 switch (number) {
    case 1:
        console.log("Units")
        break;
    case 10:
        console.log("Tens");
        break;
    case 100:
        console.log("Hundreds");
        break;
    case 1000:
        console.log("Thousands");
        break;
    default:
        console.log("Enter valid number")
        break;
}