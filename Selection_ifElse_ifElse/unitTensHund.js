var num = 10;

if ( num == 1 ) {
    console.log("Units");
}
else if ( num == 10 ) {
    console.log("Tens");
}
else if ( num == 100 ) {
    console.log("Hundreds");
}
else if ( num == 1000 ) {
    console.log("Thousands");
}
else if ( num == 10000 ) {
    console.log("Ten thousands");
}
else {
    console.log("Invalid num");
}